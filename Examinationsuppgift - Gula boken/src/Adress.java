public class Adress extends Telefonbok{

        String stad;
        String postkod;
        String gatunamn;
        String portnummer;

        public Adress(String stad, String postkod, String gatunamn, String portnummer) {
            this.stad = stad;
            this.postkod = postkod;
            this.gatunamn = gatunamn;
            this.portnummer = portnummer;
        }
    }
